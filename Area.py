width = float(input('Enter the width of the rectangle, please: '))
height = float(input('Enter the height of the rectangle, please: '))

area = width * height
perimeter = (width*2)+(height*2)

print('The area of the rectangle is: %.2f' %area)
print('The perimeter of the rectangle is: %.2f' %perimeter)
